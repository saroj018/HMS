import mongoose, { Schema, model } from "mongoose";

const patientSchema = new Schema({
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    name: {
        type: String,
        required: true
    },
    address: {
        type: String,
        required: true
    },
    phone: {
        type: String,
        required: true
    }
})

export const patientModel=mongoose.model('patient',patientSchema)